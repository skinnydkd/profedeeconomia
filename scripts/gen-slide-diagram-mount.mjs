#!/usr/bin/env node
/**
 * Generates src/components/slides/SlideDiagramMount.astro from the diagram
 * components in src/components/diagrams/. Keeps the name->component dispatch in
 * sync automatically (mirrors HerramientaIsland's static dispatch). Re-run when
 * diagrams are added: `node scripts/gen-slide-diagram-mount.mjs`.
 */
import { readdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const dir = resolve(root, 'src/components/diagrams');
const names = readdirSync(dir).filter((f) => f.endsWith('.astro')).map((f) => f.replace(/\.astro$/, '')).sort();

const imports = names.map((n) => `import ${n} from '@components/diagrams/${n}.astro';`).join('\n');
const cases = names.map((n) => `{diagrama === '${n}' && <${n} />}`).join('\n');

const out = `---
/** AUTO-GENERATED by scripts/gen-slide-diagram-mount.mjs — do not edit by hand.
 * Mounts a real diagram component by name (mirrors HerramientaIsland). */
${imports}
interface Props { diagrama: string }
const { diagrama } = Astro.props;
---
${cases}
`;

const outPath = resolve(root, 'src/components/slides/SlideDiagramMount.astro');
writeFileSync(outPath, out, 'utf8');
console.log(`✓ SlideDiagramMount.astro generated with ${names.length} diagrams.`);
